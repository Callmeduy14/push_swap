#include "push_swap.h"

void	radix_sort(t_stack **a, t_stack **b)
{
	int max_bits;
	int size;
	int i;
	int j;

	max_bits = 0;
	size = ft_stack_size(*a);
	while ((size - 1) >> max_bits != 0)
		max_bits++;
	i = 0;
	while (i < max_bits)
	{
		j = 0;
		while (j < size)
		{
			if (((*a)->index >> i) & 1)
				ra(a);
			else
				pb(a, b);
			j++;
		}
		while (ft_stack_size(*b))
			pa(a, b);
		i++;
	}
}
